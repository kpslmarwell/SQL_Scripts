Para adicionar um disco no ASM:
/etc/init.d/oracleasm createdisk [label] [device]

Para verificar um disco no ASM:
/etc/init.d/oracleasm querydisk [label]

Para escanear discos ASM:
/etc/init.d/oracleasm scandisks

Para listar discos ASM:
/etc/init.d/oracleasm listdisks

Para excluir discos ASM:
/etc/init.d/oracleasm deletedisk [label]
